package com.example.listexam01;

public class PhoneBookDTO {
    String name,tel;

    public PhoneBookDTO() {
    }

    public PhoneBookDTO(String name, String tel) {
        this.name = name;
        this.tel = tel;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
